
public class MarkerObject
{
	private int xVal,yVal,value;
	
	public MarkerObject(int value, int xVal, int yVal){
		this.xVal=xVal;
		this.yVal=yVal;
		this.value=value;
	}

	public int getxVal()
	{
		return xVal;
	}

	public int getyVal()
	{
		return yVal;
	}
	
	public int getValue()
	{
		return value;
	}
	
}
